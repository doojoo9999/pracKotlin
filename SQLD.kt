    엔터티의 종류
    - 독립 엔터티
        사람, 물건, 장소 등과 같이 현실 세계에 존재하는 엔터티
    - 업무중심 엔터티
        Transaction이 실행되면서 발생하는 엔터티
    - 종속 엔터티
        1차 정규화로 인해 관련 중심엔터티로부터 분리된 엔터티
    - 교차 엔터티
        M:M 관계를 해소하려는 목적으로 만들어진 엔터티

    ---

    유형과 무형에 따른 엔터티 종류
    - 유형 엔터티
        업무에서 도출되며 지속적으로 사용되는 엔터티
    - 개념 엔터티
        개념적으로 사용되는 엔터티. 유형 엔터티는 물리적 형태가 있지만 개념 엔터티는 없다
    - 사건 엔터티
        비즈니스 프로세스를 실행하면서 생성되는 엔터티

