    제 2 정규화	기본키 중에 특정 컬럼에만 종속된 컬럼이 존재할 경우 테이블을 분해한다.
    제 3 정규화	제 2정규형을 만족하는 상태에서 이행 함수 종속을 제거하는 정규화 과정

