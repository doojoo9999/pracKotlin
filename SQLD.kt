    엔터티의 개념 : 명사에 해당, 저장이 되기 위한 어떤 것(Thing)
    엔터티의 특징 : 업무에서 필요한 정보, 식별이 가능해야 함, 인스턴스의 집합(두개이상), 업무 프로세스에 의해 이용, 속성 포함, 관계 존재
    엔터티의 분류
    유무형 -유형 : 물리적인 형태, 안정적 (사원, 물품, 강사) -개념 : 물리적 형태 존재하지 않고 관리해야 할 개념적 정보 (조직, 보험상품) -사건 : 업무 수행함에 따라 발생됨 (주문, 청구, 미납)
    발생시점 (기본->중심-> 행위) -기본 : 원래 존재하는 정보. 독립적으로 생성 가능. (사원, 부서, 고객) -중심 : 기본엔터티로부터 발생되고 업무에 있어서 중심적인 역할 ( 계약, 접수) -행위 : 두 개 이상의 부모엔터티로부터 발생되고 자주 내용 바뀌거나 데이터 양 증가(주문내역, 계약진행)
    속성의 개념 : 업무에서 필요, 의미상 미분리, 인스턴스의 구성요소
    인스턴스 - 속성 - 엔터티 의 관계
    인스턴스 2개 이상 엔터티, 2개 이상 속성을 엔터티는 가짐, 1:1 (속성 : 속성값)
    속성의 분류
    기본속성(업무로부터 추출한 모든 속성_원래속성)
    설계 속성 (코드성 속성, 1:1치환), 파생 속성(계산된 값)

