    논리 데이터 모델링의 결과로 얻어지는 논리 데이터 모델은 데이터 모델링이 최종적으로 완료된 상태라고 정의할 수 있다.
    즉, 물리적인 스키마 설계를 하기 전 단계의 데이터 모델 상태를 일컫는 말이다.

